package com.yeelim.learn.spring.factorybean;

/**
 * @author Yeelim
 * @date 2014-2-7
 * @time 下午8:52:06
 * 
 */
public class User {

	private Integer id;
	private String name;

	/**
	 * 返回字段id
	 * 
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}

	/**
	 * 给id赋值
	 * 
	 * @param id
	 *            the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}

	/**
	 * 返回字段name
	 * 
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * 给name赋值
	 * 
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("User [id=").append(id).append(", name=").append(name)
				.append("]");
		return builder.toString();
	}
	
}
